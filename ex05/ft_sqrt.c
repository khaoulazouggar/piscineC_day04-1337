int		ft_sqrt(int nb)
{
	int i;
	int x;

	x = nb / 2;
	i = 0;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i <= x)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
